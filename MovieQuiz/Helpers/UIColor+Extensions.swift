    enum YPTheme {
        static var black: UIColor { UIColor(named: "ypBlack")! }
        static var gray: UIColor { UIColor(named: "ypGray")! }
        static var green: UIColor { UIColor(named: "ypGreen")! }
        static var red: UIColor { UIColor(named: "ypRed")! }
        static var white: UIColor { UIColor(named: "ypWhite")! }
        static var background: UIColor { UIColor(named: "ypBackground")! }
    }
}
